operation = input()
num_1 = int(input())
num_2 = int(input())
if operation == "+":
    result = num_1 + num_2
elif operation == "-":
    result = num_1 - num_2
elif operation == "*":
    result = num_1 * num_2
elif operation == "/":
    result = num_1 / num_2
elif operation == "%":
    result = num_1 % num_2
print(result)
